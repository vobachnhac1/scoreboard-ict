/**
 * ControlPanel Component
 * @param {Array} buttons - Array of button objects with { label, onClick, variant, icon, iconName, className, animate, fontBold, iconBehind }
 * @param {string} timer - Timer string to display (optional)
 * @param {boolean} showTimer - Whether to show timer (default: false)
 */
const ControlPanel = ({ buttons = [], timer = "00:00", showTimer = false }) => {
  return (
    <div className="fixed bottom-0 left-0 right-0 w-full backdrop-blur-sm z-50">
      <div className="max-w-[1920px] mx-auto px-2 p-3 mb-16">
        <div className="flex items-center justify-between gap-3">
          {/* Right: Placeholder for balance */}
          {/* {showTimer && <div className="w-[200px]"></div>} */}

          {/* Center: Buttons */}
          <div className="flex items-center justify-center gap-3 flex-wrap flex-1">
            {buttons.map((btn, btnIndex) => {
              // Xác định className dựa trên btn.variant hoặc btn.className
              let buttonClass = "bg-blue-600 hover:bg-blue-700";

              if (btn.variant === "red") {
                buttonClass = "bg-red-600 hover:bg-red-700";
              } else if (btn.variant === "yellow") {
                buttonClass = "bg-yellow-600 hover:bg-yellow-700";
              } else if (btn.variant === "green") {
                buttonClass = "bg-green-600 hover:bg-green-700";
              } else if (btn.variant === "gray") {
                buttonClass = "bg-gray-600 hover:bg-gray-700";
              } else if (btn.className) {
                // Nếu có className custom, sử dụng nó
                buttonClass = btn.className;
              }

              // Lấy icon từ iconName hoặc sử dụng icon custom
              const IconComponent = btn.iconName && Icons[btn.iconName];

              return (
                <button
                  key={btnIndex}
                  onClick={btn.onClick}
                  disabled={btn.disabled}
                  className={`${buttonClass} text-white px-4 py-2 rounded flex items-center justify-center gap-2 transition-all text-sm shadow-lg hover:shadow-xl min-w-[140px] ${
                    btn.animate ? "animate-pulse" : ""
                  } ${btn.fontBold ? "font-bold" : ""}`}
                  title={btn.tooltip || btn.label}
                >
                  {!btn.iconBehind && IconComponent ? (
                    <IconComponent />
                  ) : (
                    btn.icon
                  )}
                  {btn.label}
                  {btn.iconBehind && IconComponent ? (
                    <IconComponent />
                  ) : (
                    btn.icon
                  )}
                </button>
              );
            })}
          </div>

          {/* Left: Timer */}
          {showTimer && (
            <div className="flex items-center gap-2 bg-gray-900/80 px-6 py-3 rounded border border-yellow-500/30 shadow-lg">
              <svg
                xmlns="http://www.w3.org/2000/svg"
                className="h-5 w-5 text-yellow-400"
                viewBox="0 0 20 20"
                fill="currentColor"
              >
                <path
                  fillRule="evenodd"
                  d="M10 18a8 8 0 100-16 8 8 0 000 16zm1-12a1 1 0 10-2 0v4a1 1 0 00.293.707l2.828 2.829a1 1 0 101.415-1.415L11 9.586V6z"
                  clipRule="evenodd"
                />
              </svg>
              <span className="text-3xl font-mono font-bold tracking-wider text-yellow-300">
                {timer}
              </span>
            </div>
          )}
        </div>
      </div>
    </div>
  );
};

export default ControlPanel;
